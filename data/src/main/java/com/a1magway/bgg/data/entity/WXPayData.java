package com.a1magway.bgg.data.entity;

import java.io.Serializable;

/**
 * 微信支付之前请求服务器返回的数据结构
 * Created by jph on 2017/8/22.
 */
public class WXPayData implements Serializable{

    private static final long serialVersionUID = 7069179853637234396L;

    private String status;
    private String msg;
    private WXPayInfo obj;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public WXPayInfo getObj() {
        return obj;
    }

    public void setObj(WXPayInfo obj) {
        this.obj = obj;
    }
}
