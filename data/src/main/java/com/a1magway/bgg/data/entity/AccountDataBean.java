public class AccountDataBean {
    private int id;// – 账户id
    private String userId;// – 用户id
    private String  userType;// – 用户类别
    private String deposit;// - 存款
    private String withdrawDeposit;// -可提现金额
    private String haveWithdrawal;// – 已提现金额
    private String waitAccount;// - 待结算
    private String frozen;// -冻结款
    private String createDate;// – 创建时间
    private String modifyDate;// – 修改时间

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }

    public String getDeposit() {
        return deposit;
    }

    public void setDeposit(String deposit) {
        this.deposit = deposit;
    }

    public String getWithdrawDeposit() {
        return withdrawDeposit;
    }

    public void setWithdrawDeposit(String withdrawDeposit) {
        this.withdrawDeposit = withdrawDeposit;
    }

    public String getHaveWithdrawal() {
        return haveWithdrawal;
    }

    public void setHaveWithdrawal(String haveWithdrawal) {
        this.haveWithdrawal = haveWithdrawal;
    }

    public String getWaitAccount() {
        return waitAccount;
    }

    public void setWaitAccount(String waitAccount) {
        this.waitAccount = waitAccount;
    }

    public String getFrozen() {
        return frozen;
    }

    public void setFrozen(String frozen) {
        this.frozen = frozen;
    }

    public String getCreateDate() {
        return createDate;
    }

    public void setCreateDate(String createDate) {
        this.createDate = createDate;
    }

    public String getModifyDate() {
        return modifyDate;
    }

    public void setModifyDate(String modifyDate) {
        this.modifyDate = modifyDate;
    }
}
